package main

import (
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

// moduleMeta mirrors the fields we care about from manifest.json.
type moduleMeta struct {
	Name    string `json:"name"`
	Enabled bool   `json:"enabled"`
}

const importTemplate = `// Code generated by 'make modules'. DO NOT EDIT.
package main

import (
{{- range .}}
	_ "goapp/modules/{{.}}"
{{- end}}
)
`

func main() {
	entries, err := os.ReadDir("modules")
	if err != nil {
		fmt.Fprintf(os.Stderr, "error: cannot read modules/ directory: %v\n", err)
		os.Exit(1)
	}

	var modules []string  // all valid modules (enabled + disabled)
	var disabled []string // disabled but still imported
	var skipped []string  // invalid — no manifest or parse error

	for _, e := range entries {
		if !e.IsDir() {
			continue
		}

		dirName := e.Name()
		metaPath := filepath.Join("modules", dirName, "manifest.json")

		// Read manifest.json — skip directory if missing
		metaData, err := os.ReadFile(metaPath)
		if err != nil {
			if os.IsNotExist(err) {
				skipped = append(skipped, dirName+" (no manifest.json)")
			} else {
				fmt.Fprintf(os.Stderr, "warn: cannot read %s: %v — skipping\n", metaPath, err)
				skipped = append(skipped, dirName+" (unreadable manifest.json)")
			}
			continue
		}

		// Parse manifest.json
		var meta moduleMeta
		if err := json.Unmarshal(metaData, &meta); err != nil {
			fmt.Fprintf(os.Stderr, "warn: %s/manifest.json parse error: %v — skipping\n", dirName, err)
			skipped = append(skipped, dirName+" (invalid manifest.json)")
			continue
		}

		// Always import — even disabled modules are imported so their init()
		// registers the factory. The registry decides whether to load them
		// based on the Enabled flag. This means toggling a module on/off only
		// requires changing manifest.json, not regenerating.
		modules = append(modules, dirName)

		if !meta.Enabled {
			disabled = append(disabled, dirName)
		}
	}

	// Write modules_gen.go
	tmpl := template.Must(template.New("imports").Parse(importTemplate))

	f, err := os.Create("cmd/web/modules_gen.go")
	if err != nil {
		fmt.Fprintf(os.Stderr, "error: cannot create modules_gen.go: %v\n", err)
		os.Exit(1)
	}
	defer f.Close()

	if err := tmpl.Execute(f, modules); err != nil {
		fmt.Fprintf(os.Stderr, "error: template execution failed: %v\n", err)
		os.Exit(1)
	}

	// Summary
	fmt.Printf("✅ modules_gen.go updated\n")
	fmt.Printf("   imported : %s\n", strings.Join(modules, ", "))
	if len(disabled) > 0 {
		fmt.Printf("   disabled : %s (imported, registry will skip)\n", strings.Join(disabled, ", "))
	}
	if len(skipped) > 0 {
		fmt.Printf("   skipped  : %s\n", strings.Join(skipped, ", "))
	}
}
